import streamlit as st
import PyPDF2
import io

# ページ設定
st.set_page_config(page_title="音楽科ふりかえり分析＆次時案生成エージェント", layout="wide")

# タイトルと導入
st.title("🎵 音楽科：振り返り分析＆次時シート生成プロンプトメーカー")
st.markdown("""
このアプリは、児童の学習カード（PDF）からテキストを読み取り、
**「振り返りの分析」**と**「次回の学習シート案」**を作成するためのAI用プロンプトを自動生成します。
""")

# --- サイドバー：入力エリア ---
st.sidebar.header("1. 学習情報の入力")
meate = st.sidebar.text_area("今日の「めあて」", "例：曲想の変化を感じ取って、強弱を生かして歌おう")
contents = st.sidebar.text_area("今日の「学習内容」", "例：『赤とんぼ』の歌詞の情景を想像し、旋律の動きに合わせて歌う。")

st.sidebar.header("2. 児童データの読み込み")
uploaded_file = st.sidebar.file_uploader("学習カードのPDFをアップロード", type=['pdf'])

# --- メインエリア：処理 ---

def extract_text_from_pdf(file):
    pdf_reader = PyPDF2.PdfReader(file)
    text = ""
    for page in pdf_reader.pages:
        text += page.extract_text()
    return text

if uploaded_file is not None and meate and contents:
    st.info("PDFを読み込み中...")
    
    # PDFからテキスト抽出
    try:
        student_data_text = extract_text_from_pdf(uploaded_file)
        
        st.success("PDFの読み取りに成功しました。以下のプロンプトをコピーして、GeminiやChatGPTに貼り付けてください。")

        # --- プロンプトの構築 ---
        prompt = f"""
あなたは小学校の熟練した音楽科教師であり、教育データの分析官です。
以下の【前提データ】に基づき、児童の「振り返り」を分析し、クラス全体に共有すべき価値ある記述を抽出して、次回の学習シートを作成してください。

# 【前提データ】
## 1. 今日のめあて
{meate}

## 2. 学習内容
{contents}

## 3. 児童の学習カード記述データ（PDF読取結果）
--------------------------------------------------
{student_data_text}
--------------------------------------------------

# 【実行命令】

## 手順1：振り返りの抽出と分析
児童データの最終行付近にある「今日の振り返り」を読み取ってください。
読み取った内容を以下の2つの観点で分析してください。
1. **めあてとの整合性**: 設定しためあてに対して、どの程度達成できたか、またはどのような課題を持ったか。
2. **学習内容の受容**: 具体的な活動（歌唱、鑑賞、器楽など）を通じて、どのような「音楽的な気付き」があったか。

## 手順2：モデル・リフレクションの選出
他の児童の参考になる「前向き」かつ「分析的」な振り返りを記述している児童を3名抽出してください。
*選定基準:* 単に「楽しかった」だけでなく、「ここを工夫したら音が変わった」「友達のここが良かった」など、メタ認知が働いているものを優先すること。

## 手順3：次回の学習シートの生成
抽出した3名の記述を「みんなの素敵なお手本（キラリ！ふりかえり）」として紹介する欄を設けた、次回の学習シートの原案を作成してください。

# 【出力形式】
以下のフォーマットで出力してください。

---
## 【分析結果】
（全体の傾向を簡潔に記述）

## 【次回学習シート案】
### タイトル：[次回の題材名やテーマ]
### 学年・組・名前：[　　　　　　　　]

### 前時のキラリ！ふりかえり（紹介コーナー）
以下の友達の振り返りを読んで、今日の活動のヒントにしよう！
1. **[児童名A]** さん
   「[抽出した振り返り内容]」
   *先生からのコメント：[音楽的な良さを補足するコメント]*

2. **[児童名B]** さん
   「[抽出した振り返り内容]」
   *先生からのコメント：...*

3. **[児童名C]** さん
   「[抽出した振り返り内容]」
   *先生からのコメント：...*

### 今日のめあて
[　　　　　　　　　　　　　　　　　　]

### 今日の活動（メモ欄）
[広い空白]

### 今日の振り返り
（めあてに対してどうだったか、具体的に書こう）
[数行の罫線]
---
"""
        # プロンプトの表示とコピーボタン
        st.text_area("生成されたプロンプト", prompt, height=400)
        
    except Exception as e:
        st.error(f"エラーが発生しました: {e}")

else:
    st.warning("サイドバーから「めあて」「学習内容」を入力し、PDFをアップロードしてください。")
